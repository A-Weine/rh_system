  def create
    @user = User.new(user_params) # Cria novo usuário com os parametros do formulário de registro

    if @user.save # Testa se o usuário foi salvo
      if @user.admin?
        # Se o usuário é administrador, não precisa ter associações, visto que já tem acesso completo
      elsif @user.role == "gestor_rh"
        # Se o usuário é gestor, então são adicionadas as associações na tabela de associações.
        @user.departments = Department.where(id: params[:user][:department_ids])
      end
    else
      # Caso aconteça algum erro é redirecionado de volta para criação
      render :new
    end
  end
